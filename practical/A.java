package practical;

public class A {
	
	public void Even()
	{
		for(int i=55;i<=88;i++)
		{
			if(i%2==0)
			{
				System.out.println("Even No: "+i);
			}
		}
		
	}
	public void odd()
	{
		for(int i=155;i>=88;i--)
		{
			if(i%2!=0)
			{
				System.out.println("Odd No: "+i);
			}
		}
	}

}
